package analise.aula03;

public class AllCoffees {
	
	public void coffeeBlack(String value){
		value = "Café preto";
	}

	public void coffeeWithSugar(String value) {
		value = "Café com açucar";
		
	}
	
	public void coffeeWithCream(String value) {
		value = "Café com creme";
		
	}
	
	public void coffeeWithCreamAndSugar(String value) {
		value = "Café com creme e açucar";
		
	}
	
	

}
